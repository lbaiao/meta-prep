def icecreamParlor(m, arr):
    # Write your code here
    check_respectives = {}
    for idx, a in enumerate(arr):
        respective = m - a
        if respective in check_respectives.keys():
            if check_respectives[respective] != idx:
                result = sorted([idx + 1, check_respectives[respective] + 1])
                return result
        else:
            check_respectives[a] = idx

